void Solve() {
  int n, k;
  cin >> n >> k;
  
  bool grid[4][101] = {};
  
  int x = 1, y = 1;
    
  if (k == 2*(n-2)) {
    for (y = 1; y <= 2; ++y) for (x = 1; x <= n-2; ++x) grid[y][x] = 1;
  } else {
    while (k > 1) {
      if (x == n/2) {
        x = 1;
        ++y;
      } else {
        grid[y][x] = grid[y][n-x-1] = 1;
        k -= 2;
        ++x;
      }
    }
    if (k == 1) {
      grid[1][n/2] = 1;
    } 
  }

  cout << "YES\n";
  for (y = 0; y < 4; ++y) {
    for (x = 0; x < n; ++x) {
      if (grid[y][x]) cout << "#";
      else cout << ".";
    }
    cout << endl;
  }















}






