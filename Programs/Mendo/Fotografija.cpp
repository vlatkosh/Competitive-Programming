void Solve() {

  int n;
  cin >> n;
  
  int a[100010];
  for (int i = 0; i < n; ++i) {
    cin >> a[i];
  }
  
  ll brpogolemi[100010];
  ll brpomali[100010];
  
  ordered_set s;
  
  for (int i = 0; i < n; ++i) {
    s.insert(a[i]);
    brpogolemi[i] = s.size() - s.order_of_key(a[i]) - 1;
  }
  
  s.clear();
  for (int i = n-1; i >= 0; --i) {
    s.insert(a[i]);
    brpomali[i] = s.order_of_key(a[i]);
  }

  ll ans = 0;
  for (int i = 0; i < n; ++i) {
    ans += brpogolemi[i]*brpomali[i];
  }
  cout << ans << endl;


 



 
 
 
 
 
 
 
 
   
   
 
}
 
 
